/**
 * All available colormaps by name.
 */
export const colormaps = {
  grayscale,
  viridis,
  inferno,
  magma,
  turbo,
};

/**
 * Apply a named colormap to a normalised value.
 *
 * @param {number} normalizedValue  Value in [0, 1].
 * @param {string} colormapName     One of the keys in `colormaps`.
 * @returns {[number, number, number]}  [r, g, b] each in 0–255.
 */
export function applyColormap(normalizedValue, colormapName = 'viridis') {
  const fn = colormaps[colormapName];
  if (!fn) {
    throw new Error(`Unknown colormap: "${colormapName}". Available: ${Object.keys(colormaps).join(', ')}`);
  }
  return fn(normalizedValue);
}
