// ---- Grayscale (trivial) ---------------------------------------------------

function grayscale(t) {
  const v = Math.round(clamp01(t) * 255);
  return [v, v, v];
}

// ---- Helper ----------------------------------------------------------------

function clamp01(t) {
  return t < 0 ? 0 : t > 1 ? 1 : t;
}

/**
 * Given a 256-entry LUT (array of [r,g,b] with values in 0-1), return a
 * colormap function that maps a normalised value to [r,g,b] in 0-255.
 */
function makeLutColormap(lut) {
  return function (t) {
    const idx = Math.round(clamp01(t) * 255);
    const c = lut[idx];
    return [Math.round(c[0] * 255), Math.round(c[1] * 255), Math.round(c[2] * 255)];
  };
}

